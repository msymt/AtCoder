use proconio::input;
use proconio::marker::Chars;

fn main() {
    input!{
        a:i32,
        b:i32,
        c:i32,
    }
    let mut v: Vec<i32> = vec![];
    v.push(a);
    v.push(b);
    v.push(c);
    v.sort();
    if v == [5, 5, 7] {
        println!("YES");
    }else {
        println!("NO");
    }
}
