use proconio::input;
use proconio::marker::Chars;

fn main() {
    input!{
        s: String,
    }
    let sun = "Sunny";
    let cld = "Cloudy";
    let ran = "Rainy";
    if s == sun {
        println!("{}", cld);
    } else if s == cld {
        println!("{}", ran);
    } else {
        println!("{}", sun);
    }
}
